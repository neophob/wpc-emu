'use strict';

import test from 'ava';
import checksum from '../../../../lib/boards/memory/checksum';

test('Checksum: calculate MM Highscore Checksum', (t) => {
  const result = checksum.checksum16(new Uint8Array(
    [ 0x42, 0x52, 0x45, 0x0, 0x44, 0x0, 0x0, 0x0,
      0x4C, 0x46, 0x53, 0x0, 0x40, 0x0, 0x0, 0x0,
      0x5A, 0x41, 0x50, 0x0, 0x36, 0x0, 0x0, 0x0,
      0x52, 0x43, 0x46, 0x0, 0x32, 0x0, 0x0, 0x0 ]));
  t.deepEqual(result, 0xFB8F);
});
